#include "glic/colorgenerator.h"
using namespace std;

CglicColorGenerator::CglicColorGenerator(){
  srand(time(0));
  start = (rand()/(double)(RAND_MAX + 1)) + 1;
  inc = 0;
}

CglicColorGenerator::~CglicColorGenerator(){

}

glm::vec3 CglicColorGenerator::generateColor(){
  double golden_ratio_conjugate = 0.618033988749895;
  float h = (start + inc * golden_ratio_conjugate);
  h -= int(h);
  rgb rgbColor = hsv2rgb(hsv{double(h)*360, 0.7, 0.9});
  inc++;
  glm::vec3 mix = glm::vec3(1);
  glm::vec3 col = glm::vec3(rgbColor.r, rgbColor.g, rgbColor.b);
  float mixFactor = 0.4;//1 = pure color, 0 = pure white
  return mixFactor * col + (1-mixFactor) * mix;
}

hsv CglicColorGenerator::rgb2hsv(rgb in){
    hsv         out;
    double      min, max, delta;

    min = in.r < in.g ? in.r : in.g;
    min = min  < in.b ? min  : in.b;

    max = in.r > in.g ? in.r : in.g;
    max = max  > in.b ? max  : in.b;

    out.v = max;                                // v
    delta = max - min;
    if( max > 0.0 ) { // NOTE: if Max is == 0, this divide would cause a crash
        out.s = (delta / max);                  // s
    } else {
        // if max is 0, then r = g = b = 0
            // s = 0, v is undefined
        out.s = 0.0;
        out.h = NAN;                            // its now undefined
        return out;
    }
    if( in.r >= max )                           // > is bogus, just keeps compilor happy
        out.h = ( in.g - in.b ) / delta;        // between yellow & magenta
    else
    if( in.g >= max )
        out.h = 2.0 + ( in.b - in.r ) / delta;  // between cyan & yellow
    else
        out.h = 4.0 + ( in.r - in.g ) / delta;  // between magenta & cyan

    out.h *= 60.0;                              // degrees

    if( out.h < 0.0 )
        out.h += 360.0;

    return out;
}


rgb CglicColorGenerator::hsv2rgb(hsv in){
    double      hh, p, q, t, ff;
    long        i;
    rgb         out;

    if(in.s <= 0.0) {       // < is bogus, just shuts up warnings
        out.r = in.v;
        out.g = in.v;
        out.b = in.v;
        return out;
    }
    hh = in.h;
    if(hh >= 360.0) hh = 0.0;
    hh /= 60.0;
    i = (long)hh;
    ff = hh - i;
    p = in.v * (1.0 - in.s);
    q = in.v * (1.0 - (in.s * ff));
    t = in.v * (1.0 - (in.s * (1.0 - ff)));

    switch(i) {
    case 0:
        out.r = in.v;
        out.g = t;
        out.b = p;
        break;
    case 1:
        out.r = q;
        out.g = in.v;
        out.b = p;
        break;
    case 2:
        out.r = p;
        out.g = in.v;
        out.b = t;
        break;

    case 3:
        out.r = p;
        out.g = q;
        out.b = in.v;
        break;
    case 4:
        out.r = t;
        out.g = p;
        out.b = in.v;
        break;
    case 5:
    default:
        out.r = in.v;
        out.g = p;
        out.b = q;
        break;
    }
    return out;
}








